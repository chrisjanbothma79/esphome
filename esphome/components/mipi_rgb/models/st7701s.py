from esphome.components.mipi import LOGGER, MADCTL, MADCTL_ML, MADCTL_XFLIP, DriverChip
from esphome.config_validation import UNDEFINED
from esphome.const import CONF_COLOR_ORDER, CONF_MIRROR_X, CONF_MIRROR_Y

SDIR_CMD = 0xC7


class ST7701S(DriverChip):
    def add_madctl(self, sequence: list, config: dict):
        transform = self.get_transform(config)
        if self.rotation_as_transform(config):
            LOGGER.info("Using hardware transform to implement rotation")
        madctl = 0x00
        if config.get(CONF_COLOR_ORDER, "RGB") == "BGR":
            madctl |= 0x08
        if transform.get(CONF_MIRROR_Y, False):
            madctl |= MADCTL_ML
        sequence.append((MADCTL, madctl))
        sdir = 0
        if transform.get(CONF_MIRROR_X, False):
            sdir |= 0x04
            madctl |= MADCTL_XFLIP
        sequence.append((SDIR_CMD, sdir))
        return madctl


